package ajou.ac.kr.teaming.vo;

public class MessageVO {

    String user_name;
    String user_message;
    String user_message_date;

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_message() {
        return user_message;
    }

    public void setUser_message(String user_message) {
        this.user_message = user_message;
    }

    public String getUser_message_date() {
        return user_message_date;
    }

    public void setUser_message_date(String user_message_date) {
        this.user_message_date = user_message_date;
    }
}
